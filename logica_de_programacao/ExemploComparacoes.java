package logica_de_programacao;
public class ExemploComparacoes {
    public static void main(String[] args) {
        // Exemplo de comparações
        int x = 5;
        int y = 10;

        // Igualdade (==)
        boolean igual = x == y;
        System.out.println("Igualdade: " + igual);

        // Desigualdade (!=)
        boolean diferente = x != y;
        System.out.println("Desigualdade: " + diferente);

        // Maior que (>)
        boolean maiorQue = x > y;
        System.out.println("Maior que: " + maiorQue);

        // Menor que (<)
        boolean menorQue = x < y;
        System.out.println("Menor que: " + menorQue);

        // Maior ou igual a (>=)
        boolean maiorOuIgualA = x >= y;
        System.out.println("Maior ou igual a: " + maiorOuIgualA);

        // Menor ou igual a (<=)
        boolean menorOuIgualA = x <= y;
        System.out.println("Menor ou igual a: " + menorOuIgualA);
    }
}
